public class WhileDoWhile {
    public static void main(String[] args) {

        // while
        System.out.println("While");
        int i = 1;
        while (i < 1) {
            System.out.println(i);
            i++;
        }

        // Do - While
        System.out.println("Do-While");
        int j = 1;
        do {
            System.out.println(j);
            j++;
        } while (j < 1);
    }

    /*
     * Buat menggunakan While
     * 
     * @params int i = 8
     * 1 x 2 = 2
     * 2 x 2 = 4
     * 3 x 2 = 6
     * 4 x 2 = 8
     * 5 x 2 = 10
     * 6 x 2 = 12
     * 7 x 2 = 14
     * 8 x 2 = 16
     * 9 x 2 = 18
     * 10 x 2 = 20
     * 
     */
}
